public class Contact
{
    private string from;
    private string area;
    private string subject;
    private string message;

    public string From
    {
        get { return from; }
        set { from = value; }
    }

    public string Area
    {
        get { return area; }
        set { area = value; }
    }

    public string Subject
    {
        get { return subject; }
        set { subject = value; }
    }

    public string Message
    {
        get { return message; }
        set { message = value; }
    }
}